# In order to print It's "kind off" sunny we have 2 methods. The first is as follows

a = '''It's "kind off" sunny'''
print(a)

# For the second method we can use an Escape Sequence as follows

b = "\t It\\'s \"kind off\" sunny \n Hope you like it"
print(b)


# You will get an error if you use double quotes inside a string that is surrounded by double quotes
# To fix this problem, use the escape character \"
# The escape character allows you to use double quotes when you normally would not be allowed
