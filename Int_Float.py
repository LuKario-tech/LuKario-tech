# Fubdamental Data Types
int and float

# int (Integers) are basically numbers from 0 to infinity
# 2 + 4 (Addition)
# 3 - 2 (Subtraction)
# 4 / 2 (Division)
# 4 * 2 (Multiplication)
# 4 ** 2 (Exponents)
# 4 // 2 (Find Quotient)
# 4 % 2 (Modulo to find remainders)

print(2 + 4)
print(3 - 2)
print(4 / 2)
print(4 * 2)
print(4 ** 2)
print(3 // 2)
print(4 % 2)

# type function is used to determine which types of data type is
# there in your program

print(type(2 + 4))
print(type(3 - 2))
print(type(4 / 2))
print(type(4 * 4))
print(type(4 ** 2))
print(type(3 // 2))
print(type(4 % 2))
print(type(0))
print(type(2 + 1.34))
print(type(9.9 + 1.1))

# Math Functions
print(round(3.9693534, 2)) # The round() function returns a floating point number
# that is a rounded version of the specified number, with the specified number of decimals.
# The default number of decimals is 0,
# meaning that the function will return the nearest integer.

print(abs(-1232.213)) # The abs() function returns the absolute value of the specified number.

