# Booleans in Python are a data type that represents the concept of truth.
# A boolean value can either be True or False. In Python,
# the keywords True and False (with capital T and F) are used to represent
# boolean values.
# Booleans are commonly used in conditional statements and logical operations
# to control the flow of a program or express logical relationships
# between different values or conditions.

print(bool(1))
print(bool(0))